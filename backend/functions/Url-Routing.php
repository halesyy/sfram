<?php
  class url {
    # Required class vars.
      var $site_path;

    # Setting the site-path.
      function __construct($site_path) {
        $this->site_path = $this->removeSlash($site_path);
      }

    # This is called when the user tries to call the class variable - example: $a = new url(), echo $a = echoes the site_path.
      function __toString() {
        return $this->site_path;
      }

    # Removes slash from the string if there is one at the end.
      private function removeSlash($string) {
        if ($string[strlen($string) - 1] == '/')
          $string = rtrim($string, '/');

        return $string;
      }

    # Returns a specific segment of the page.
      public function segment($segment_num) {
        # Will return the entire site URL without domain - example: localhost/first-slug/second-slug = /first-slug/second-slug [is returned].
          $all_slugs = str_replace($this->site_path, '', $_SERVER['REQUEST_URI']);
        # Splits the slugs into individual pieces.
          $slugs = explode('/', $all_slugs);
        # Return management.
          if (isset($slugs[$segment_num])) {
            # The slug exists.
            return $slugs[$segment_num]
          } else {
            # The slug wasn't found.
            return false;
          }
      }
        # REWRITES FOR THE SLUG SELECTION.
        public function first() { return $this->segment(1); }
        public function second() { return $this->segment(2); }
        public function third() { return $this->segment(3); }
        public function fourth() { return $this->segment(4); }
        public function fifth() { return $this->segment(5); }
        public function sixth() { return $this->segment(6); }

